#include <stdio.h>

int	ft_strcmp(char *s1, char *s2);

int	main(void)
{
	char	a[] = "ABRA";
	char	b[] = "ABR";

	printf("%d", ft_strcmp(a, b));
}

int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;

	i = 0;
	while (s1[i] != '\0' && s1[i])
	{
		if (s1[i] != s2[i])
		{
			break ;
		}
		i++;
	}
	return (s1[i] - s2[i]);
}
